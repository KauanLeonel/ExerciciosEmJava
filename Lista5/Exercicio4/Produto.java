package Lista5.Exercicio4;

public abstract class Produto {
    private String nome;
    private float valor;

    

    //#region Getters and Setters
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public float getValor() {
        return valor;
    }
    public void setValor(float valor) {
        this.valor = valor;
    }
    //#endregion
    
}
